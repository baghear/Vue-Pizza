import Vue from 'vue'
import VueRouter from 'vue-router'
import Main from '../components/main/Main.vue'
import Cart from '../components/cart/Cart.vue'

Vue.use(VueRouter)

const routes = [
  {
    path: '/',
    name: 'Main',
    component: Main
  },
  {
    path: '/cart',
    name: 'Cart',
    component: Cart
  }
]

const router = new VueRouter({
  mode: 'history',
  base: process.env.BASE_URL,
  routes
})

export default router
